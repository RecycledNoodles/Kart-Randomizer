package kart_randomizer.model;

public class Track {
	private String name;
	private String cup;
	
	public Track() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCup() {
		return cup;
	}

	public void setCup(String cup) {
		this.cup = cup;
	}
}
